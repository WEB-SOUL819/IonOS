// Create a new animation
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
Animation* AnimationManager::createAnimation(AnimationType type, uint32_t duration_ms) {
    for (int i = 0; i < MAX_ANIMATIONS; i++) {
        if (!animations[i].active) {
            animations[i].type = type;
            animations[i].duration_ms = duration_ms;
            animations[i].easing = EASE_LINEAR;
            animations[i].active = false;
            animations[i].callback = nullptr;
            return &animations[i];
        }
    }
    return nullptr;  // No free animation slots
}

