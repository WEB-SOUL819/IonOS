// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
bool UIManager::switchScreen(UIScreen screen, UITransition transition) {
    if (screen == current_screen) {
        return true;
    }

    // Push current screen to stack
    if (stack_depth < MAX_STACK_DEPTH) {
        screen_stack[stack_depth] = current_screen;
        stack_depth++;
    }

    previous_screen = current_screen;
    current_screen = screen;
    active_transition = transition;
    transition_start_time = millis();
    dirty = true;

    Serial.printf("[UI] Switched to screen %d\n", screen);
    return true;
}

