void UIManager::render() {
    if (!dirty && active_transition == TRANSITION_NONE) {
        return;
    }

    DisplayDriver::clear();

    // Render transition effect if active
    if (active_transition != TRANSITION_NONE) {
        renderTransition();
    }

    // Render status bar if visible
    if (status_bar_visible) {
        renderStatusBar();
    }

    dirty = false;
}

