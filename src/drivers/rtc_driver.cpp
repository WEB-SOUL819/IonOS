// Static member initialization
bool RTCDriver::initialized = false;

// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// Initialize RTC
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
bool RTCDriver::init() {
    if (initialized) {
        return true;
    }

    // Initialize I2C (should already be done for display, but ensure it's set up)
    Wire.begin(RTC_SDA, RTC_SCL, RTC_I2C_FREQ);

    // Check if device responds
    Wire.beginTransmission(RTC_I2C_ADDR);
    uint8_t error = Wire.endTransmission();
    
    if (error != 0) {
        Serial.printf("[RTC] I2C error: %d\n", error);
        return false;
    }

    // Read current time to verify
    DateTime dt;
    getTime(dt);

    Serial.println("[RTC] Initialized DS3231 RTC");
    initialized = true;
    return true;
}

