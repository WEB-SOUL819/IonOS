// Handle power events
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
void Kernel::handlePowerEvents() {
    static uint32_t last_check = 0;

    if ((millis() - last_check) < 1000) {  // Check every second
        return;
    }
    last_check = millis();

    // Check battery
    if (PowerManager::isCriticalBattery() && !PowerManager::isCharging()) {
        Event event = {
            .type = EVENT_POWER_LOW_BATTERY,
            .priority = PRIORITY_CRITICAL,
            .timestamp = millis(),
            .data1 = PowerManager::getBatteryPercentage(),
            .data2 = 0,
            .ptr = nullptr
        };
        postEvent(event);
    }

    // Check charging
    static bool was_charging = false;
    bool is_charging = PowerManager::isCharging();
    if (is_charging != was_charging) {
        was_charging = is_charging;
        Event event = {
            .type = EVENT_POWER_CHARGING,
            .priority = PRIORITY_NORMAL,
            .timestamp = millis(),
            .data1 = is_charging ? 1 : 0,
            .data2 = 0,
            .ptr = nullptr
        };
        postEvent(event);
    }
}

