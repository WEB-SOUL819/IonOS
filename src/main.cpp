// Handle serial debug commands
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
void handleSerialDebug() {
    if (!Serial.available()) {
        return;
    }

    String command = Serial.readStringUntil('\n');
    command.trim();

    if (command.length() == 0) {
        return;
    }

    Serial.println();  // Echo newline

    // Debug commands
    if (command == "info") {
        Kernel::printDebugInfo();
    } else if (command == "mem") {
        Kernel::printMemoryInfo();
    } else if (command == "help") {
        printDebugHelp();
    } else if (command == "restart") {
        Serial.println("[DEBUG] Restarting...");
        ESP.restart();
    } else if (command == "sleep") {
        Serial.println("[DEBUG] Entering light sleep...");
        // PowerManager::lightSleep(5000);
    } else if (command == "test-display") {
        testDisplay();
    } else if (command == "test-buttons") {
        testButtons();
    } else if (command == "test-battery") {
        testBattery();
    } else if (command == "test-rtc") {
        testRTC();
    } else {
        Serial.printf("[DEBUG] Unknown command: %s\n", command.c_str());
        Serial.println("[DEBUG] Type 'help' for commands");
    }

    Serial.println();
}

